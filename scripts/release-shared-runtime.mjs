function runNpmOrThrow(args) {
  if (npmExecPath) {
    runOrThrow(process.execPath, [npmExecPath, ...args], { env: process.env });
    return;
  }
  runOrThrow(npmCommand, args, { env: process.env });
}

function runOrThrow(command, args, options = {}) {
  const result = spawnSync(command, args, {
    cwd: options.cwd ?? projectRoot,
    stdio: "inherit",
    env: {
      ...process.env,
      CI: process.env.CI ?? "true",
      ...(options.env ?? {}),
    },
  });
  if (result.error) {
    const message = result.error instanceof Error ? result.error.message : String(result.error);
    throw new Error(`Failed to run command: ${command} ${args.join(" ")}\n${message}`);
  }
  if (result.status !== 0) {
    throw new Error(`Command failed (${result.status}): ${command} ${args.join(" ")}`);
  }
}

function applyRuntimeExecutableBranding({
  executablePath,
  iconPath,
  version,
  productName,
  executableName,
  companyName,
}) {
  if (!existsSync(executablePath)) {
    throw new Error(`Runtime executable not found at ${executablePath}`);
  }

  runOrThrow(
    rceditPath,
    [
      executablePath,
      "--set-icon",
      iconPath,
      "--set-file-version",
      version,
      "--set-product-version",
      version,
      "--set-version-string",
      "FileDescription",
      productName,
      "--set-version-string",
      "ProductName",
      productName,
      "--set-version-string",
      "InternalName",
      executableName,
      "--set-version-string",
      "OriginalFilename",
      `${executableName}.exe`,
      "--set-version-string",
      "CompanyName",
      companyName,
    ],
    {
      env: process.env,
    },
  );
}

function pruneRuntimeLocales(runtimeDir, allowedLocaleFiles) {
  const localesDir = join(runtimeDir, "locales");
  if (!existsSync(localesDir)) {
    return;
  }

  const entries = readdirSync(localesDir, { withFileTypes: true });
  for (const entry of entries) {
    if (!entry.isFile()) {
      continue;
    }

    const fileName = String(entry.name ?? "").trim();
    if (!fileName.toLowerCase().endsWith(".pak")) {
      continue;
    }
    if (allowedLocaleFiles.has(fileName)) {
      continue;
    }

    removePath(join(localesDir, fileName));
  }
}

function normalizeVersion(rawVersion) {
  return String(rawVersion ?? "")
    .trim()
    .replace(/^[^\d]*/, "");
}

function archiveDirectory(sourceDir, outputArchivePath) {
  if (existsSync(outputArchivePath)) {
    rmSync(outputArchivePath, { force: true });
  }
  runOrThrow(sevenZipPath, ["a", "-tzip", "-mx=9", outputArchivePath, ".\\*"], {
    env: process.env,
    cwd: sourceDir,
  });
}

function buildPackageMetadata(archivePath, archiveName) {
  const size = statSync(archivePath).size;
  const sha256 = computeSha256(archivePath);
  return {
    name: archiveName,
    url: `${releaseBaseUrl}${encodeURIComponent(archiveName)}`,
    sha256,
    size,
  };
}

function computeSha256(filePath) {
  const hash = createHash("sha256");
  const buffer = readFileSync(filePath);
  hash.update(buffer);
  return hash.digest("hex");
}

function computeDirectorySize(directoryPath) {
  if (!existsSync(directoryPath)) {
    return 0;
  }
  let total = 0;
  const stack = [directoryPath];
  while (stack.length > 0) {
    const current = stack.pop();
    if (!current) {
      continue;
    }
    const entries = readdirSync(current, { withFileTypes: true });
    for (const entry of entries) {
      const target = join(current, entry.name);
      if (entry.isDirectory()) {
        stack.push(target);
      } else if (entry.isFile()) {
        total += statSync(target).size;
      }
    }
  }
  return total;
}

function toMB(bytes) {
  return Number((bytes / (1024 * 1024)).toFixed(2));
}

function writeJson(targetPath, payload) {
  writeFileSync(targetPath, `${JSON.stringify(payload, null, 2)}\n`, "utf8");
}

function removePath(targetPath) {
  if (!existsSync(targetPath)) {
    return;
  }
  rmSync(targetPath, { recursive: true, force: true });
}

function readBooleanFlag(rawValue, fallbackValue) {
  const normalized = String(rawValue ?? "").trim().toLowerCase();
  if (!normalized) {
    return fallbackValue;
  }
  return !["0", "false", "off", "no"].includes(normalized);
}

function bumpPackagePatchVersion(packageJsonPath) {
  const rawPackageJson = readFileSync(packageJsonPath, "utf8");
  const parsedPackageJson = JSON.parse(rawPackageJson);
  const currentVersion = String(parsedPackageJson.version ?? "").trim();
  const semverMatch = currentVersion.match(/^(\d+)\.(\d+)\.(\d+)$/);
  if (!semverMatch) {
    throw new Error(`Invalid package version "${currentVersion}". Expected format X.Y.Z.`);
  }
  const nextVersion = `${semverMatch[1]}.${semverMatch[2]}.${Number.parseInt(semverMatch[3], 10) + 1}`;
  parsedPackageJson.version = nextVersion;
  writeFileSync(packageJsonPath, `${JSON.stringify(parsedPackageJson, null, 2)}\n`, "utf8");
  process.stdout.write(`[release:shared-runtime] version bumped: ${currentVersion} -> ${nextVersion}\n`);
}

function normalizeBaseUrl(rawUrl) {
  const normalized = String(rawUrl ?? "").trim();
  if (!normalized) {
    return "";
  }
  return normalized.endsWith("/") ? normalized : `${normalized}/`;
}

function resolveGitHubToken() {
  const token =
    String(
      process.env.GH_TOKEN ??
        process.env.GITHUB_TOKEN ??
        process.env.MESSLY_UPDATER_TOKEN ??
        "",
    ).trim() || "";
  if (!token) {
    throw new Error("Missing GitHub token. Set GH_TOKEN (or GITHUB_TOKEN / MESSLY_UPDATER_TOKEN).");
  }
  return token;
}

async function publishArtifactsToGitHub({ token, owner, repo, tag, releaseName, files }) {
  const apiBase = `https://api.github.com/repos/${owner}/${repo}`;
  const release = await getOrCreateRelease({
    apiBase,
    token,
    tag,
    releaseName,
  });

  const uploadUrl = String(release.upload_url ?? "").replace("{?name,label}", "");
  if (!uploadUrl) {
    throw new Error("GitHub release upload URL not available.");
  }

  const existingAssets = Array.isArray(release.assets) ? release.assets : [];
  for (const filePath of files) {
    const fileName = basename(filePath);
    const existing = existingAssets.find((asset) => String(asset?.name ?? "") === fileName);
    if (existing?.id) {
      await githubRequest({
        method: "DELETE",
        url: `${apiBase}/releases/assets/${existing.id}`,
        token,
      });
    }

    const content = readFileSync(filePath);
    process.stdout.write(`[release:shared-runtime] uploading ${fileName}...\n`);
    await githubRequest({
      method: "POST",
      url: `${uploadUrl}?name=${encodeURIComponent(fileName)}`,
      token,
      headers: {
        "Content-Type": "application/octet-stream",
        "Content-Length": String(content.byteLength),
      },
      body: content,
      responseType: "json",
    });
  }
}

async function getOrCreateRelease({ apiBase, token, tag, releaseName }) {
  try {
    return await githubRequest({
      method: "GET",
      url: `${apiBase}/releases/tags/${encodeURIComponent(tag)}`,
      token,
      responseType: "json",
    });
  } catch (error) {
    const isNotFound = error instanceof Error && error.message.includes("404");
    if (!isNotFound) {
      throw error;
    }
  }

  return githubRequest({
    method: "POST",
    url: `${apiBase}/releases`,
    token,
    headers: {
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      tag_name: tag,
      name: releaseName,
      draft: false,
      prerelease: false,
      generate_release_notes: true,
    }),
    responseType: "json",
  });
}

async function githubRequest({
  method,
  url,
  token,
  headers = {},
  body,
  responseType = "text",
}) {
  const response = await fetch(url, {
    method,
    headers: {
      Authorization: `Bearer ${token}`,
      Accept: "application/vnd.github+json",
      "X-GitHub-Api-Version": "2022-11-28",
      ...headers,
    },
    body,
  });

  if (!response.ok) {
    const text = await response.text();
    throw new Error(`GitHub API ${method} ${url} failed (${response.status}): ${text}`);
  }

  if (responseType === "json") {
    return response.json();
  }
  return response.text();
}
